type TranslationKey = keyof typeof messages.en;

export function normalizeLocale(language: string | undefined | null): AppLocale {
  const lower = (language ?? '').toLowerCase();
  if (lower.startsWith('zh')) return 'zh-CN';
  if (lower.startsWith('ja')) return 'ja';
  return 'en';
}

function isLocalePreference(value: string | null): value is LocalePreference {
  return value === 'auto' || value === 'en' || value === 'zh-CN' || value === 'ja';
}

export function getLocalePreference(): LocalePreference {
  try {
    const value = globalThis.localStorage?.getItem(LOCALE_STORAGE_KEY) ?? null;
    return isLocalePreference(value) ? value : 'auto';
  } catch {
    return 'auto';
  }
}

export function setLocalePreference(locale: LocalePreference): void {
  try {
    if (locale === 'auto') {
      globalThis.localStorage?.removeItem(LOCALE_STORAGE_KEY);
      return;
    }
    globalThis.localStorage?.setItem(LOCALE_STORAGE_KEY, locale);
  } catch {
    // Ignore storage failures; callers still get browser-language fallback.
  }
}

export function getCurrentLocale(): AppLocale {
  const preference = getLocalePreference();
  if (preference !== 'auto') return preference;
  return normalizeLocale(globalThis.navigator?.language);
}

type TranslationParams = Record<string, string | number>;

function interpolate(message: string, params: TranslationParams): string {
  let result: string = message;
  for (const [k, v] of Object.entries(params)) {
    result = result.replace(`$${k}`, String(v));
  }
  return result;
}

export function t(
  key: TranslationKey,
  paramsOrLocale: TranslationParams | AppLocale = getCurrentLocale(),
  locale: AppLocale = getCurrentLocale(),
): string {
  const hasParams = typeof paramsOrLocale === 'object';
  const resolvedLocale = hasParams ? locale : paramsOrLocale;
  const message = messages[resolvedLocale][key] ?? messages.en[key];
  return hasParams ? interpolate(message, paramsOrLocale) : message;
}

export function tf(
  key: TranslationKey,
  params: TranslationParams,
  locale: AppLocale = getCurrentLocale(),
): string {
  return t(key, params, locale);
}

export function formatCount(
  value: number,
  singularKey: TranslationKey,
  locale: AppLocale = getCurrentLocale(),
): string {
  return `${value} ${t(singularKey, locale)}`;
}
